package operators;

public class AssignOperators {
    public static void main(String[] args) {
        int q = 10;
        System.out.println(q);  // 10

        q += 2;  // q = q + 2;
        System.out.println(q);  // 12

        q -= 2;  // q = q -2
        System.out.println(q);  // 10

        q *= 2;  // q = q * 2
        System.out.println(q);  // 20

        q /= 2;  // q = q / 2
        System.out.println(q);  // 20

        q %= 2;  // q = q % 2
        System.out.println(q);  // 0

    }
}
