package ifelseoperators;

public class Example2 {
    public static void main(String[] args) {
        // например, если у нас будет хорошая погода, то мы идем кататься на лодке, иначе мы пойдем в ресторан

        boolean goodWeather = true;

        if (goodWeather) {
            System.out.println("Поедем какаться на лодке");
        } else {
            System.out.println("Поход в ресторан");
        }

        System.out.println("end of file");

    }
}
